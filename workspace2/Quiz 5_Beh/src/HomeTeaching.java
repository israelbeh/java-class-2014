    private void BtnCalcHTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnCalcHTMouseClicked
       DecimalFormat dFormat = new DecimalFormat("0.00");//rounds the percentage to 2 decimals
       DecimalFormat drFormat = new DecimalFormat("0");//allows the percentage to be displayed as a whole number
       
       int iCount;
       int iTotal = 0;
       double dPercentage = 0;
       
     //the array holds the checkboxes
     JCheckBox aChkBxHT[] = {ChkbxHT1, ChkbxHT2, ChkbxHT3, ChkbxHT4, ChkbxHT5,
       ChkbxHT6, ChkbxHT7, ChkbxHT8, ChkbxHT9, ChkbxHT10};
   //the for loop goes through each check box
        for (iCount = 0; iCount < aChkBxHT.length; iCount++)
        {
            //the if statement verifies if the checkbox is selected
         if (aChkBxHT[iCount].isSelected())
         {
              iTotal +=1;
         }
        else
            iTotal += 0;
        }
    // dPercentage holds the percentage of HT that is checked.
    dPercentage = (double) iTotal / aChkBxHT.length;
    dFormat.format(dPercentage);
    dPercentage = dPercentage*100;//makes the percentage a whole number.
    LblCalcPct.setText((drFormat.format(dPercentage)) + " %");
    }//GEN-LAST:event_BtnCalcHTMouseClicked

