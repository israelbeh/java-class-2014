    private void BtnResetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnResetMouseClicked
    
        int iCount;
   
       JCheckBox aChkBxHT[] = {ChkbxHT1, ChkbxHT2, ChkbxHT3, ChkbxHT4, ChkbxHT5,
       ChkbxHT6, ChkbxHT7, ChkbxHT8, ChkbxHT9, ChkbxHT10};
       // for loop goes through the array of checkboxes
        for (iCount = 0; iCount < aChkBxHT.length; iCount++)
        {
            //the if statement reset the checkbox as unchecked
            if (aChkBxHT[iCount].isSelected())
            {
                aChkBxHT[iCount].setSelected(false);// unchecks the boxes
            }
        }
    
        LblCalcPct.setText(null);// resets the perentage
    
    }//GEN-LAST:event_BtnResetMouseClicked

